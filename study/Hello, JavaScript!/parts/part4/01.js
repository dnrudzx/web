var a=10, b=20;

console.log(a+b);           //더하기
console.log(a-b);           //빼기
console.log(a*b);           //곱하기
console.log(b/a);           //나누기
console.log(a%3);           //나머지

console.log(++a);           //이번라인에서 1을 더해줌
console.log(a);
console.log(a++);           //일단 출력하고 1을 더함
console.log(a);

console.log(Math.pow(2,3)); //A의 B승
console.log(Math.sqrt(a));  //A의 제곱근
console.log(Math.random()); //0~1사이 임의 난수 발생